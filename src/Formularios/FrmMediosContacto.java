    private void ModificarTelefono(int id_medio, String telefono) {
        PreparedStatement pst;
        try {
            if (txtdescripcion.getText().length() != 10) {
                JOptionPane.showMessageDialog(null, "EL NUMERO TELEFONICO DEBE SER DE 10 DIGITOS", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                pst = cn.prepareStatement("UPDATE telefono SET numero='" + telefono + "' WHERE id_medio='" + id_medio + "'");
                int s = JOptionPane.showConfirmDialog(null, "¿ESTAS SEGURO QUE DESEAS MODIFICAR EL NUMERO TELEFÓNICO?", "CONFIRMACION", 0);
                if (s == 0) {
                    pst.executeUpdate();
                    BuscarporTelefono();
                    Limpiar();
                    JOptionPane.showMessageDialog(null, "DATOS ACTUALIZADOS CORRECTAMENTE");
                }
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "HA OCURRIDO UN ERROR " + e, "Error inesperado", JOptionPane.ERROR_MESSAGE);
        }
    }

