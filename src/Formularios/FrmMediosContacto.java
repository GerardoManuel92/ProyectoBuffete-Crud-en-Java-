    private void MostrarContactos() {
        Statement st;
        ResultSet rs;
        String sql = "SELECT descripcion FROM tipo_medio ORDER BY id_tipo ASC";
        try {
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                jcb_contacto.addItem(rs.getString("descripcion"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL CARGAR" + e, "ERROR INESPERADO", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void ValidarContacto() {
        if (jcb_contacto.getSelectedIndex() == 1) {
            BuscarporTelefono();
        } else if (jcb_contacto.getSelectedIndex() == 2) {
            BuscarporCorreo();
        } else if (jcb_contacto.getSelectedIndex() == 3) {
            BuscarporFacebook();
        } else {
            JOptionPane.showMessageDialog(null, "OPCION NO VALIDA", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void BuscarporTelefono() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID CLIENTE");
        modelo.addColumn("ID PATERNO");
        modelo.addColumn("ID MATERNO");
        modelo.addColumn("NOMBRE");       
        modelo.addColumn("ID MEDIO");
        modelo.addColumn("CONTACTO");
        tabla_clientes.setModel(modelo);
        Statement st;
        ResultSet rs;
        String dato[] = new String[6];
        

        try {
            String sql = "select cl.id_cliente, cl.paterno, cl.materno, cl.nombre,mc.id_medio,t.numero from cliente cl, telefono t, medio_contacto mc, tipo_medio tm "
                + "where tm.id_tipo='"+lbl_tipo.getText()+"' and cl.id_cliente='"+txt_idcliente.getText()+"' and cl.id_cliente=mc.id_cliente and t.id_medio=mc.id_medio and mc.id_tipo=tm.id_tipo group by t.numero";
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {                
                dato[0] = rs.getString(1);
                dato[1] = rs.getString(2);
                dato[2] = rs.getString(3);
                dato[3] = rs.getString(4);
                dato[4] = rs.getString(5);   
                dato[5] = rs.getString(6); 
                modelo.addRow(dato);
            }            
            tabla_clientes.setModel(modelo);            
            if(tabla_clientes.getRowCount()==0) {
                JOptionPane.showMessageDialog(null, "EL CLIENTE NO REGISTRO NINGUN CONTACTO DE ESTE TIPO", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL CARGAR" + e, "ERROR INESPERADO", JOptionPane.ERROR_MESSAGE);
        }
    }

