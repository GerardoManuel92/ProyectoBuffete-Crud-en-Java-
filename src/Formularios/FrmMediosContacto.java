    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        if (txtdescripcion.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "NO HAS INGRESADO EL NUMERO, CORREO O PERFIL A GUARDAR", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            int id_medio = Integer.parseInt(lbl_medio.getText());
            String telefono = txtdescripcion.getText();
            String correo = txtdescripcion.getText();
            String cuenta = txtdescripcion.getText();

            if (jcb_contacto.getSelectedIndex() == 1) {
                ModificarTelefono(id_medio, telefono);
            } else if (jcb_contacto.getSelectedIndex() == 2) {
                ModificarCorreo(id_medio, correo);
            } else if (jcb_contacto.getSelectedIndex() == 3) {
                ModificarFacebook(id_medio, cuenta);
            }
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

